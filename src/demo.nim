import raylib

# ----------------------------------------------------------------------------------------
# Global Variables Definition
# ----------------------------------------------------------------------------------------

const
    screenWidth = 800
    screenHeight = 450

# ----------------------------------------------------------------------------------------
# Program main entry point
# ----------------------------------------------------------------------------------------

proc main =
    # Initialization
    # --------------------------------------------------------------------------------------

    initWindow(screenWidth, screenHeight, "slapdash demo")
    defer: closeWindow()

    setTargetFPS(60)
    
    
    # --------------------------------------------------------------------------------------
    while not windowShouldClose():
        # Update
        # ------------------------------------------------------------------------------------
        

        # ------------------------------------------------------------------------------------
        # Draw
        # ------------------------------------------------------------------------------------

        beginDrawing()
        defer: endDrawing()
        clearBackground(RayWhite)
        drawText("slapdash demo", 190, 200, 20, LightGray)

        # ------------------------------------------------------------------------------------
    # De-Initialization
    # --------------------------------------------------------------------------------------
  
main()